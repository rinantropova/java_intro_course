public class strings {
    public static void main(String[] args) {
        String[] name = {"S", "e", "r", "g", "e", "i"};
        String sk = "SERGEI KA.";
        System.out.println(sk.toLowerCase());
        System.out.println(String.join("", name));
        System.out.println(String.join("", "S", "e", "r", "g", "e", "i"));
        System.out.println(String.join(",", "S", "e", "r", "g", "e", "i"));;
    }
}
